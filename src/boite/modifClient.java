    public void afficher() {
        form_client act = new form_client();
        String id = act.getValeurId();
        
        try {
            String requete = "SELECT * FROM CLIENT WHERE idcli = ?";
            Ps = conn.prepareStatement(requete);
            Ps.setString(1, id);
            rs = Ps.executeQuery();
            
            if(rs.next()) {
                String t1 = rs.getString("idcli");
                String t2 = rs.getString("nom");
                String t3 = rs.getString("mail");
                String t4 = rs.getString("contact");
                idClient.setText(t1);
                nom.setText(t2);
                email.setText(t3);
                
            }
            
        } catch(Exception e) {
            System.out.println("--> Exception " +e);
        }
    } 
