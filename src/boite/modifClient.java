    private void modifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifActionPerformed
         form_client act = new form_client();
         String id = act.getValeurId();
         
         
        if (idClient.getText().isEmpty() || nom.getText().isEmpty() || contact.getText().isEmpty() || email.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,
                "Veuillez remplir tout les champs .",
                "Champ vide",
                JOptionPane.WARNING_MESSAGE);
                return;
        }
         
        try {
            String requete = "UPDATE CLIENT set nom = ? ,mail = ? ,contact = ? WHERE idcli = ?";
           
            Ps = conn.prepareStatement(requete);
           
            Ps.setString(3,id);
            Ps.setString(1,nom.getText());
            Ps.setString(2,email.getText());
            Ps.setString(4,contact.getText());
            Ps.executeUpdate();

            JOptionPane.showMessageDialog(null,"Ajouter avec succes");
        }catch(Exception e) {
            System.out.println("---> Exception " + e);

        }finally {
            try{
                //  rs.close();
                Ps.close();

            }catch (Exception e){
                System.out.println("--> Exception" + e);
            }
        }
    }//GEN-LAST:event_modifActionPerformed

