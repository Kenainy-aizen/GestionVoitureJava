    private void modifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifActionPerformed

        
        java.util.Date utilDate = date.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String strDate = sdf.format(utilDate);
        
        if(numAchat.getText().isEmpty() || idClient.getText().isEmpty() || idVoiture.getText().isEmpty() || nombre.getText().isEmpty() || date.getDate() == null ) {
            
              JOptionPane.showMessageDialog(this,
                "Veuillez remplir tout les champs .",
                "Champ vide",
                JOptionPane.WARNING_MESSAGE);
                return;
        }
        
        try {
            String requete = "UPDATE ACHAT set idcli = ? ,idvoit = ?,date = ? ,qte = ? WHERE numAchat = ? ";
            Ps = conn.prepareStatement(requete);
            Ps.setString(5,numAchat.getText());
            Ps.setString(1,idClient.getText());
            Ps.setString(2,idVoiture.getText());
            Ps.setString(3,strDate);
            Ps.setString(4,nombre.getText());

            Ps.executeUpdate();

            JOptionPane.showMessageDialog(null,"Modifier avec succes");
        }catch(Exception e) {
            System.out.println("---> Exception " + e);

        }finally {
            try{  Ps.setString(4,nombre.getText());
                rs.close();
                Ps.close();

            }catch (Exception e){
                System.out.println("--> Exception" + e);
            }
        }
    }//GEN-LAST:event_modifActionPerformed

