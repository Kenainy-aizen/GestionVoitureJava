    public void afficher() {
        form_achat act = new form_achat();
        String id  = act.getNumAchat();
        
        try {
            String requete = "SELECT * FROM ACHAT WHERE numAchat = ?";
            Ps = conn.prepareStatement(requete);
            Ps.setString(1, id);
            rs = Ps.executeQuery();
            
            if(rs.next()) {
                String t1 = rs.getString("numAchat");
                String t2 = rs.getString("idcli");
                String t3 = rs.getString("idvoit");
               java.sql.Date sqlDate = rs.getDate("date");
                String t5 = rs.getString("qte");
                
                numAchat.setText(t1);
                idClient.setText(t2);
                idVoiture.setText(t3);
                date.setDate(sqlDate);
                nombre.setText(t5);
            }
            
        } catch (Exception e) {
            System.out.println("--> Exception " +e);
        }
        
        
    }
